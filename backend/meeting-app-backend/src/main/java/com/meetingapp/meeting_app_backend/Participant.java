package com.meetingapp.meeting_app_backend;

public class Participant {

    private String name;
    private String color;


    public Participant(String name, String color) {
        this.name = name;
        this.color = color;
    }


    public String getName() {
        return name;
    }


    public String getColor() {
        return color;
    }


    

    
}